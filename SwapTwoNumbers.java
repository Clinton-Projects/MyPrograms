public class SwapTwoNumbers {
    public static void main(String[] args){
    int a=10;
    int b=20;
    int c=30;
    int d;
    System.out.println("Before Swapping :" +a+" "+b+" "+c);   
    d=a;  //this is swaping
    a=b;
    b=c;
    c=d;
    System.out.println("After Swapping :"+ a+" "+b+" "+c);    
}
}
